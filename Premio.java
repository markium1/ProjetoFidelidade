package ProjetoFidelidade;

public class Premio {

    int codPremio;
    String nomePremio;
    int qtPontos;

    public int getCodPremio() {
        return codPremio;
    }

    public void setCodPremio(int codPremio) {
        this.codPremio = codPremio;
    }

    public String getNomePremio() {
        return nomePremio;
    }

    public void setNomePremio(String nomePremio) {
        this.nomePremio = nomePremio;
    }

    public int getQtPontos() {
        return qtPontos;
    }

    public void setQtPontos(int qtPontos) {
        this.qtPontos = qtPontos;
    }
}
